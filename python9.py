import math
#ต้องไว้ยบนสุด

x = min(5, 10, 25)
# หาค่าน้อยที่สุด
y = max(10, 5, 25)
# หาค่ามากที่สุด
print(x)
print(y)

z = abs (-7.25)
print(z)

v = pow (4,3) # 4*4*4 = 64
# ค่ายกกำลัง
print(v)

a = math.sqrt(64)
print(a)

x = math.ceil(1.3)
# ปัดขึ้น เป็น 2
y = math.floor(1.3)
# ปัดลง เป็น 1

print(x)
print(y)